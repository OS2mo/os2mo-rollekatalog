import uuid
from datetime import datetime, timedelta

import pytest
from fastramqpi.pytest_util import retry
from httpx import AsyncClient

from os2mo_rollekatalog.autogenerated_graphql_client import GraphQLClient


@pytest.mark.integration_test
async def test_dummy() -> None:
    return


@pytest.mark.integration_test
async def test_too_much(
    test_client: AsyncClient,
    graphql_client: GraphQLClient,
    root_uuid: uuid.UUID,
) -> None:
    # Create org hierarchy
    org_unit_type_uuid = (
        (await graphql_client._testing__get_org_unit_type()).objects[0].uuid
    )
    await graphql_client._testing__create_org_unit_root(
        root_uuid=root_uuid,
        name="Root",
        org_unit_type=org_unit_type_uuid,
    )
    layer1_1 = (
        await graphql_client._testing__create_org_unit(
            name="Layer 1 - Unit 1",
            parent=root_uuid,
            org_unit_type=org_unit_type_uuid,
        )
    ).uuid
    layer1_2 = (
        await graphql_client._testing__create_org_unit(
            name="Layer 1 - Unit 2",
            parent=root_uuid,
            org_unit_type=org_unit_type_uuid,
        )
    ).uuid
    layer2_1 = (
        await graphql_client._testing__create_org_unit(
            name="Layer 2 - Unit 1",
            parent=layer1_2,
            org_unit_type=org_unit_type_uuid,
        )
    ).uuid
    layer3_1 = (
        await graphql_client._testing__create_org_unit(
            name="Layer 3 - Unit 1",
            parent=layer2_1,
            org_unit_type=org_unit_type_uuid,
        )
    ).uuid

    @retry()
    async def validate_hierarchy() -> None:
        assert (await test_client.get(f"/cache/org_unit/{root_uuid}")).json() == {
            "uuid": str(root_uuid),
            "name": "Root",
            "parentOrgUnitUuid": None,
            "manager": None,
            "klePerforming": [],
            "kleInterest": [],
        }
        assert (await test_client.get(f"/cache/org_unit/{layer1_1}")).json() == {
            "uuid": str(layer1_1),
            "name": "Layer 1 - Unit 1",
            "parentOrgUnitUuid": str(root_uuid),
            "manager": None,
            "klePerforming": [],
            "kleInterest": [],
        }
        assert (await test_client.get(f"/cache/org_unit/{layer1_2}")).json() == {
            "uuid": str(layer1_2),
            "name": "Layer 1 - Unit 2",
            "parentOrgUnitUuid": str(root_uuid),
            "manager": None,
            "klePerforming": [],
            "kleInterest": [],
        }
        assert (await test_client.get(f"/cache/org_unit/{layer2_1}")).json() == {
            "uuid": str(layer2_1),
            "name": "Layer 2 - Unit 1",
            "parentOrgUnitUuid": str(layer1_2),
            "manager": None,
            "klePerforming": [],
            "kleInterest": [],
        }
        assert (await test_client.get(f"/cache/org_unit/{layer3_1}")).json() == {
            "uuid": str(layer3_1),
            "name": "Layer 3 - Unit 1",
            "parentOrgUnitUuid": str(layer2_1),
            "manager": None,
            "klePerforming": [],
            "kleInterest": [],
        }

    await validate_hierarchy()

    await graphql_client._testing__rename_org_unit(layer1_1, "New Name")

    @retry()
    async def observe_name_change() -> None:
        assert (await test_client.get(f"/cache/org_unit/{layer1_1}")).json() == {
            "uuid": str(layer1_1),
            "name": "New Name",
            "parentOrgUnitUuid": str(root_uuid),
            "manager": None,
            "klePerforming": [],
            "kleInterest": [],
        }

    await observe_name_change()

    # Create employees
    anders_and = (
        await graphql_client._testing__create_employee(
            first_name="Anders",
            last_name="And",
        )
    ).uuid
    fedtmule = (
        await graphql_client._testing__create_employee(
            first_name="Fedt",
            last_name="mule",
        )
    ).uuid
    joakim = (
        await graphql_client._testing__create_employee(
            first_name="Joakim",
            last_name="von And",
        )
    ).uuid
    user_without_sam_account = (
        await graphql_client._testing__create_employee(
            first_name="Sam",
            last_name="Mangler",
        )
    ).uuid
    fætter_højben = (
        await graphql_client._testing__create_employee(
            first_name="Fætter",
            last_name="Højben",
        )
    ).uuid

    # Create engagements ("positions"). Note that Joakim does not work.
    engagement_type = (
        (await graphql_client._testing__get_engagement_type())
        .objects[0]
        .current.classes[0]  # type: ignore
        .uuid
    )
    job_function = (
        (await graphql_client._testing__get_job_function())
        .objects[0]
        .current.classes[0]  # type: ignore
    )
    anders_and_eng = (
        await graphql_client._testing__create_engagement(
            layer1_2, anders_and, engagement_type, job_function.uuid
        )
    ).uuid
    fedtmule_eng = (
        await graphql_client._testing__create_engagement(
            layer3_1, fedtmule, engagement_type, job_function.uuid
        )
    ).uuid
    fætter_eng = (
        await graphql_client._testing__create_engagement(
            layer3_1, fætter_højben, engagement_type, job_function.uuid
        )
    ).uuid

    # Create Active Directory IT system and users
    AD = (await graphql_client._testing__create_it_system("Active Directory")).uuid
    FK = (await graphql_client._testing__create_it_system("FK ORG")).uuid

    await graphql_client._testing__create_it_user(
        AD, str(uuid.uuid4()), joakim, "JvA", []
    )

    anders_object_guid = uuid.uuid4()
    anders_external_id = uuid.uuid4()
    await graphql_client._testing__create_it_user(
        AD, str(anders_object_guid), anders_and, "AA", [anders_and_eng]
    )
    await graphql_client._testing__create_it_user(
        FK,
        str(anders_external_id),
        anders_and,
        str(anders_object_guid),
        [anders_and_eng],
    )

    fedtmule_object_guid = uuid.uuid4()
    fedtmule_external_id = uuid.uuid4()
    fedtmule_ad_ituser = await graphql_client._testing__create_it_user(
        AD, str(fedtmule_object_guid), fedtmule, "FM", [fedtmule_eng]
    )
    await graphql_client._testing__create_it_user(
        FK,
        str(fedtmule_external_id),
        fedtmule,
        str(fedtmule_object_guid),
        [fedtmule_eng],
    )
    await graphql_client._testing__update_it_user(
        fedtmule_ad_ituser.uuid, datetime.now() + timedelta(days=365)
    )

    fætter_object_guid = uuid.uuid4()
    fætter_external_id = uuid.uuid4()
    await graphql_client._testing__create_it_user(
        AD,
        str(fætter_object_guid),
        fætter_højben,
        "FH",
        [fætter_eng],
        datetime.now() + timedelta(days=365),
    )
    await graphql_client._testing__create_it_user(
        FK,
        str(fætter_external_id),
        fætter_højben,
        str(fætter_object_guid),
        [fætter_eng],
    )

    @retry()
    async def verify_users() -> None:
        assert (
            await test_client.get(f"/debug/person/{user_without_sam_account}")
        ).json() == {"error": "No SAM Account"}
        assert (await test_client.get(f"/debug/person/{joakim}")).json() == []

        assert (await test_client.get(f"/cache/person/{anders_and}")).json() == [
            {
                "extUuid": str(anders_external_id),
                "person": str(anders_and),
                "userId": "AA",
                "name": "Anders And",
                "email": None,
                "positions": [
                    {
                        "name": job_function.user_key,
                        "orgUnitUuid": str(layer1_2),
                    }
                ],
            }
        ]
        assert (await test_client.get(f"/cache/person/{fedtmule}")).json() == [
            {
                "extUuid": str(fedtmule_external_id),
                "person": str(fedtmule),
                "userId": "FM",
                "name": "Fedt mule",
                "email": None,
                "positions": [
                    {
                        "name": job_function.user_key,
                        "orgUnitUuid": str(layer3_1),
                    }
                ],
            }
        ]
        assert (await test_client.get(f"/cache/person/{fætter_højben}")).json() == [
            {
                "extUuid": str(fætter_external_id),
                "person": str(fætter_højben),
                "userId": "FH",
                "name": "Fætter Højben",
                "email": None,
                "positions": [
                    {
                        "name": job_function.user_key,
                        "orgUnitUuid": str(layer3_1),
                    }
                ],
            }
        ]

    await verify_users()

    # Move org out of synced tree. This should also remove our users from cache
    await graphql_client._testing__move_org_unit_to_root(layer1_2)

    @retry()
    async def verify_removal() -> None:
        assert (await test_client.get(f"/cache/person/{joakim}")).json() == []
        assert (await test_client.get(f"/cache/person/{anders_and}")).json() == []
        assert (await test_client.get(f"/cache/person/{fedtmule}")).json() == []
        assert (await test_client.get(f"/cache/org_unit/{layer1_2}")).json() is None
        assert (await test_client.get(f"/cache/org_unit/{layer2_1}")).json() is None
        assert (await test_client.get(f"/cache/org_unit/{layer3_1}")).json() is None

    await verify_removal()
